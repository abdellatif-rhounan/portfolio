    public function index()
    {
        $projects = Project::select('id', 'name', 'description', 'link', 'img_src')->get()
            ->map(function ($project) {
                return [
                    'id' => $project->id,
                    'name' => $project->name,
                    'description' => $project->description,
                    'link' => $project->link,
                    'img_src' => $project->img_src,
                    'technologies' => $project->technologies->map(function ($technologie) {
                        return $technologie->name;
                    }),
                ];
            });

        return response($projects, 200);
    }

